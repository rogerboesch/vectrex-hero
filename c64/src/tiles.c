/*
 * tiles.c — Initialize VIC-II charset and colors
 *
 * Copies the tileset into the custom charset RAM at $3000.
 * Sets VIC-II memory pointers to use the custom charset.
 */

#include "game.h"
#include "tiles.h"

/* Placeholder charset: will be replaced with exported tileset data */
/* For now, generate basic tiles programmatically */

void tiles_init(void) {
    uint16_t i;

    /* Point VIC-II to charset at $2000 */
    /* $D018: bits 4-7 = screen offset, bits 1-3 = charset offset */
    /* Screen at $0400: (0x0400/0x400)=1 -> bits 4-7 = 0001 */
    /* Charset at $2000: (0x2000/0x800)=4 -> bits 1-3 = 100 */
    VIC_MEMPTR = (1 << 4) | (4 << 1);  /* = 0x18 */

    /* Clear charset RAM */
    memset(CHARSET_RAM, 0, 2048);

    /* Tile 0: empty (already zero) */

    /* Tile 1: solid block */
    for (i = 0; i < 8; i++)
        CHARSET_RAM[1 * 8 + i] = 0xFF;

    /* Tile 2: checkerboard */
    for (i = 0; i < 8; i++)
        CHARSET_RAM[2 * 8 + i] = (i & 1) ? 0x55 : 0xAA;

    /* Tile 3: border block */
    CHARSET_RAM[3 * 8 + 0] = 0xFF;
    CHARSET_RAM[3 * 8 + 1] = 0x81;
    CHARSET_RAM[3 * 8 + 2] = 0x81;
    CHARSET_RAM[3 * 8 + 3] = 0x81;
    CHARSET_RAM[3 * 8 + 4] = 0x81;
    CHARSET_RAM[3 * 8 + 5] = 0x81;
    CHARSET_RAM[3 * 8 + 6] = 0x81;
    CHARSET_RAM[3 * 8 + 7] = 0xFF;

    /* HUD tiles */
    /* Tile 96 (TILE_HUD_FILL): solid bar */
    for (i = 0; i < 8; i++)
        CHARSET_RAM[96 * 8 + i] = (i == 0 || i == 7) ? 0x7E : 0x7E;

    /* Tile 97 (TILE_HUD_EMPTY): outline bar */
    CHARSET_RAM[97 * 8 + 0] = 0x7E;
    for (i = 1; i < 7; i++)
        CHARSET_RAM[97 * 8 + i] = 0x00;
    CHARSET_RAM[97 * 8 + 7] = 0x7E;

    /* Tile 98 (TILE_HEART) */
    CHARSET_RAM[98 * 8 + 0] = 0x00;
    CHARSET_RAM[98 * 8 + 1] = 0x6C;
    CHARSET_RAM[98 * 8 + 2] = 0xFE;
    CHARSET_RAM[98 * 8 + 3] = 0xFE;
    CHARSET_RAM[98 * 8 + 4] = 0x7C;
    CHARSET_RAM[98 * 8 + 5] = 0x38;
    CHARSET_RAM[98 * 8 + 6] = 0x10;
    CHARSET_RAM[98 * 8 + 7] = 0x00;

    /* Generate digit font (0-9) at tiles 112-121 */
    {
        static const uint8_t digit_font[10][7] = {
            {0x0E,0x11,0x13,0x15,0x19,0x11,0x0E},
            {0x04,0x0C,0x04,0x04,0x04,0x04,0x0E},
            {0x0E,0x11,0x01,0x06,0x08,0x10,0x1F},
            {0x0E,0x11,0x01,0x06,0x01,0x11,0x0E},
            {0x02,0x06,0x0A,0x12,0x1F,0x02,0x02},
            {0x1F,0x10,0x1E,0x01,0x01,0x11,0x0E},
            {0x06,0x08,0x10,0x1E,0x11,0x11,0x0E},
            {0x1F,0x01,0x02,0x04,0x08,0x08,0x08},
            {0x0E,0x11,0x11,0x0E,0x11,0x11,0x0E},
            {0x0E,0x11,0x11,0x0F,0x01,0x02,0x0C},
        };
        uint8_t d, row;
        for (d = 0; d < 10; d++) {
            CHARSET_RAM[(112 + d) * 8] = 0;
            for (row = 0; row < 7; row++)
                CHARSET_RAM[(112 + d) * 8 + row + 1] = digit_font[d][row] << 2;
        }
    }

    /* Generate letter font (A-Z) at tiles 122-147 */
    {
        static const uint8_t letter_font[26][7] = {
            {0x0E,0x11,0x11,0x1F,0x11,0x11,0x11}, /* A */
            {0x1E,0x11,0x11,0x1E,0x11,0x11,0x1E}, /* B */
            {0x0E,0x11,0x10,0x10,0x10,0x11,0x0E}, /* C */
            {0x1E,0x09,0x09,0x09,0x09,0x09,0x1E}, /* D */
            {0x1F,0x10,0x10,0x1E,0x10,0x10,0x1F}, /* E */
            {0x1F,0x10,0x10,0x1E,0x10,0x10,0x10}, /* F */
            {0x0E,0x11,0x10,0x17,0x11,0x11,0x0F}, /* G */
            {0x11,0x11,0x11,0x1F,0x11,0x11,0x11}, /* H */
            {0x0E,0x04,0x04,0x04,0x04,0x04,0x0E}, /* I */
            {0x07,0x02,0x02,0x02,0x02,0x12,0x0C}, /* J */
            {0x11,0x12,0x14,0x18,0x14,0x12,0x11}, /* K */
            {0x10,0x10,0x10,0x10,0x10,0x10,0x1F}, /* L */
            {0x11,0x1B,0x15,0x15,0x11,0x11,0x11}, /* M */
            {0x11,0x11,0x19,0x15,0x13,0x11,0x11}, /* N */
            {0x0E,0x11,0x11,0x11,0x11,0x11,0x0E}, /* O */
            {0x1E,0x11,0x11,0x1E,0x10,0x10,0x10}, /* P */
            {0x0E,0x11,0x11,0x11,0x15,0x12,0x0D}, /* Q */
            {0x1E,0x11,0x11,0x1E,0x14,0x12,0x11}, /* R */
            {0x0E,0x11,0x10,0x0E,0x01,0x11,0x0E}, /* S */
            {0x1F,0x04,0x04,0x04,0x04,0x04,0x04}, /* T */
            {0x11,0x11,0x11,0x11,0x11,0x11,0x0E}, /* U */
            {0x11,0x11,0x11,0x0A,0x0A,0x04,0x04}, /* V */
            {0x11,0x11,0x11,0x15,0x15,0x1B,0x11}, /* W */
            {0x11,0x11,0x0A,0x04,0x0A,0x11,0x11}, /* X */
            {0x11,0x11,0x0A,0x04,0x04,0x04,0x04}, /* Y */
            {0x1F,0x01,0x02,0x04,0x08,0x10,0x1F}, /* Z */
        };
        uint8_t d, row;
        for (d = 0; d < 26; d++) {
            CHARSET_RAM[(122 + d) * 8] = 0;
            for (row = 0; row < 7; row++)
                CHARSET_RAM[(122 + d) * 8 + row + 1] = letter_font[d][row] << 2;
        }
    }

    /* Symbols: - : . at tiles 148-150 */
    {
        static const uint8_t sym_font[3][7] = {
            {0x00,0x00,0x00,0x1F,0x00,0x00,0x00}, /* - */
            {0x00,0x00,0x04,0x00,0x00,0x04,0x00}, /* : */
            {0x00,0x00,0x00,0x00,0x00,0x00,0x04}, /* . */
        };
        uint8_t d, row;
        for (d = 0; d < 3; d++) {
            CHARSET_RAM[(148 + d) * 8] = 0;
            for (row = 0; row < 7; row++)
                CHARSET_RAM[(148 + d) * 8 + row + 1] = sym_font[d][row] << 2;
        }
    }

    /* Set border and background colors */
    VIC_BORDER = COLOR_BLACK;
    VIC_BG0 = COLOR_BLACK;
}
